package org.ever._4ever_be_business.hr.enums;

public enum TrainingCategory {

    INTERNSHIP,         // 인턴십: 정식 채용 전 실무 경험 중심의 프로그램
    ONBOARDING,         // 온보딩: 신입/이직자 대상 회사 적응 및 기본교육
    TRAINING,           // 일반 직무교육: 업무 역량 강화를 위한 사내/외 교육
    WORKSHOP,           // 워크숍: 팀 단위 문제 해결, 협업, 조직문화 중심의 단기 프로그램
    SEMINAR,            // 세미나: 특정 주제에 대한 단기 발표/토론 세션
    COURSE,             // 코스형 교육: 정규 과정 형태의 교육 (예: 데이터 분석 기초 4주 과정)
    CERTIFICATION,      // 자격 취득 교육: 자격증 준비나 인증 관련 교육
    COMPLIANCE,         // 준법/윤리 교육: 법정의무교육, 개인정보보호, 성희롱 방지 등
    SAFETY,             // 안전교육: 산업안전, 보건, 소방 등 필수 안전 관련 교육
    LEADERSHIP,         // 리더십 교육: 관리자, 팀장 대상 리더십/코칭 스킬 강화
    TECHNICAL,          // 기술교육: IT/개발/엔지니어링 등 전문 기술 중심
    SOFT_SKILL,         // 소프트스킬: 커뮤니케이션, 협업, 문제해결 등 비기술 역량
    LANGUAGE,           // 어학 교육: 영어, 일본어 등 언어 역량 향상
    SALES,              // 영업/CS 관련 교육
    MANAGEMENT,         // 경영, 전략, 재무 등 관리직 중심 교육
    EXTERNAL,           // 외부 위탁 교육: 외부 기관/대학/온라인 플랫폼 교육
    MANDATORY,          // 법정의무교육: 정부/기관에서 의무화된 교육
    MENTORING,          // 멘토링/코칭 프로그램
    OTHER               // 기타 교육
}